class Square {
  constructor(x, y, h, w, color) {
    this.x = x;
    this.y = y;
    this.h = h;
    this.w = w;
    this.color = color;
  }

  setX(x) {
    this.x = x;
  }

  setY(y) {
    this.y = y;
  }

  setH(h) {
    this.h = h;
  }

  setW(w) {
    this.w = w;
  }

  setColor(color) {
    this.color = color;
  }
}
